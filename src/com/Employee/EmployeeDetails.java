package com.Employee;

public class EmployeeDetails 
{
	String name;
	String gender;
	String age;
	String phonenbr;
	String id;
	String joiningDate;
	String experience;
	String skills;
	public EmployeeDetails(String name, String gender, String age, String phonenbr, String id, String joiningDate, String experience, String skills)
	{
		this.name=name;
		this.gender=gender;
		this.age=age;
		this.phonenbr=phonenbr;
		this.id=id;
		this.joiningDate=joiningDate;
		this.experience=experience;
		this.skills=skills;		
	}
	public EmployeeDetails()
	{
		
	}
	
	

}